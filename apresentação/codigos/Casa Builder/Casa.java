public class Casa{
    private int janela;
    private int portas;
    private int comodos;
    private boolean piscina;
    private boolean jardim;
    private boolean estatuas;
    private boolean garagem;

    public int getJanela() {
        return this.janela;
    }

    public void setJanela(int janela) {
        this.janela = janela;
    }

    public int getPortas() {
        return this.portas;
    }

    public void setPortas(int portas) {
        this.portas = portas;
    }

    public int getComodos() {
        return this.comodos;
    }

    public void setComodos(int comodos) {
        this.comodos = comodos;
    }
    
    public boolean isPiscina() {
        return this.piscina;
    }

    public boolean getPiscina() {
        return this.piscina;
    }

    public void setPiscina(boolean piscina) {
        this.piscina = piscina;
    }

    public boolean isJardim() {
        return this.jardim;
    }

    public boolean getJardim() {
        return this.jardim;
    }

    public void setJardim(boolean jardim) {
        this.jardim = jardim;
    }

    public boolean isEstatuas() {
        return this.estatuas;
    }

    public boolean getEstatuas() {
        return this.estatuas;
    }

    public void setEstatuas(boolean estatuas) {
        this.estatuas = estatuas;
    }

    public boolean isGaragem() {
        return this.garagem;
    }

    public boolean getGaragem() {
        return this.garagem;
    }

    public void setGaragem(boolean garagem) {
        this.garagem = garagem;
    }

}